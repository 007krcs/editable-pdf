import { describe, it, expect } from 'vitest';
import { PDFDocument } from 'pdf-lib';
import { embedImage } from '../src/image-embedder.js';

// Minimal valid 1x1 PNG (67 bytes)
const PNG_BYTES = new Uint8Array([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // PNG signature
  0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, // 8-bit RGB
  0xde, 0x00, 0x00, 0x00, 0x0c, 0x49, 0x44, 0x41, // IDAT chunk
  0x54, 0x08, 0xd7, 0x63, 0xf8, 0xcf, 0xc0, 0x00,
  0x00, 0x00, 0x02, 0x00, 0x01, 0xe2, 0x21, 0xbc,
  0x33, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, // IEND chunk
  0x44, 0xae, 0x42, 0x60, 0x82,
]);

describe('embedImage', () => {
  it('should embed a PNG image', async () => {
    const doc = await PDFDocument.create();
    const image = await embedImage(doc, PNG_BYTES);

    expect(image.width).toBe(1);
    expect(image.height).toBe(1);
  });

  it('should throw for unsupported format', async () => {
    const doc = await PDFDocument.create();
    const badBytes = new Uint8Array([0x00, 0x01, 0x02, 0x03]);

    await expect(embedImage(doc, badBytes)).rejects.toThrow('Unsupported image format');
  });
});
